package com.psa.application.model;

public class IncidentCommImpact 
{
	private String channel;
	private String natureOfImpact;
	private String volumeOfImpact;
	
	public String getChannel() {
		return channel;
	}
	public void setChannel(String channel) {
		this.channel = channel;
	}
	public String getNatureOfImpact() {
		return natureOfImpact;
	}
	public void setNatureOfImpact(String natureOfImpact) {
		this.natureOfImpact = natureOfImpact;
	}
	public String getVolumeOfImpact() {
		return volumeOfImpact;
	}
	public void setVolumeOfImpact(String volumeOfImpact) {
		this.volumeOfImpact = volumeOfImpact;
	}
	
	
}
